				Sindarin is a language which was created by J.R.R. Tolkien starting in 1917. Back then the name of the language Tolkien worked on was “Goldorin” alors called “Gnomic”. It was renamed “Noldorin” in the twenties and then Sindarin during the fifties. The linguist was working simultaneously on Qenya, which he started in 1915, a language which is to Sindarin what Latin is to French. This one was later renamed “Quenya”.<br>
				Sindarin is one of the elvish languages Tolkien created we can find in his books. When the elvish population divided so did the languages. The Sindars, the grey elves, are the one who spoke Sindarin. After the One Ring was destroyed, it became the language to speak for the elves.<br>
				<br>
