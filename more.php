			<h1 class="u"> Determiners </h1>

			<ul>

				<li> 
					There is no indefinite article in Sindarin.<br>
					<span class="tab u">Example :</span> <em>a man = adan</em>
				</li>

				<li>
					The definite article in Sindarin is <strong>i</strong>.<br>
					<span class="tab u">Example :</span> the man = i adan<br>
					It becomes <strong>ir</strong> in front of a word which starts with an "i".<br>
					<span class="tab u">Example :</span> <em>ir ithil = the moon</em><br>
					In front of a pluparl noun, it becomes <strong>in</strong>.<br>
					<span class="tab u">Example :</span> <em>the men = in edain</em>
				</li>

				<li>
					The genitive article in Sindarin is <strong>en</strong>. It is only used when the "owner" is a general substantive, it does not work with proper nouns nor known owners.<br>
					<span class="tab u">Example:</span> <em>cabed en aras = the deer's bounce // Aran Gondor = Gondor's king</em><br>
					&nbsp;&nbsp;&nbsp; > This article can be found under the <strong>e</strong> form but it is exactly the same.<br>
					The plural genitive article is <strong>in</strong> and works the same way.
				</li>
			</ul>

			<h1 class="u"> The "s" or "ss" problem </h1>
			
			<p>
				People who write and speak Sindarin are divied about the "s" or "ss" writing in monosyllabic words. The two forms can be found and it does not really matter because the oral difference is very small and subtle. Yet we will usually prefer the "ss" writing.
			</p>

